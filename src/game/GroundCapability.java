package game;

/**
 * An enum representing a collection of constants which indicate the capabilities of a Ground.
 *
 * @author Siang Jo Yee
 */
public enum GroundCapability {
    /**
     * A Ground is capable of being sowed on it.
     */
    CAN_BE_SOWED_ON,

    /**
     * A Ground is capable of being fertilized.
     */
    CAN_BE_FERTILIZED,

    /**
     * A Ground is capable of being harvested.
     */
    CAN_BE_HARVESTED
}
