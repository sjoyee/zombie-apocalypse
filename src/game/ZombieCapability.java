package game;

/**
 * An enum representing a collection of constants which indicate the capabilities of an Actor.
 *
 */
public enum ZombieCapability {
	/**
	 * An Actor is undead like an zombie.
	 */
	UNDEAD,

	/**
	 * An Actor is alive.
	 */
	ALIVE,

	/**
	 * An Actor is capable of farming.
	 */
	FARM,
	CHANT,
	MAMBO
}
